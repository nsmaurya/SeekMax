        return color(named: "textPrimary")
    }
    
    static var secondaryText: UIColor {
        return color(named: "textSecondary")
    }
    
    static var reversedText: UIColor {
        return color(named: "textReversed")
    }
    
    static var primaryButton: UIColor {
        return color(named: "button")
    }
}
